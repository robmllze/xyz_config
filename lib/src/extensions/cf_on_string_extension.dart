extension CfOnStringExtension on String {
  /// Maps the string to using the active config file.
  T? cf<T>(
    Config config, [
    Map<dynamic, dynamic> args = const {},
  ]) {
    return config.map<T>(
      this,
      args: args,
    );
  }
}