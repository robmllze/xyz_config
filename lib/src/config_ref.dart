class ConfigRef<T> {
  //
  //
  //

  final T refCode;

  //
  //
  //

  const ConfigRef(this.refCode);

  //
  //
  //

  @override
  String toString() => this.refCode.toString();

  //
  //
  //

  @override
  bool operator ==(final other) =>
      other is ConfigRef && other.hashCode == this.hashCode;

  //
  //
  //

  @override
  int get hashCode => this.refCode.hashCode;
}
