extension TrOnStringExtension on String {
  String tr([
    Map<dynamic, dynamic> args = const {},
  ]) {
    return ConfigManager.translationManager!.map<String>(
          this,
          args: args,
          fallback: this,
        ) ??
        this;
  }
}
