extension XyzConfigTranslate on String {
  T? tr<T>([Map<dynamic, dynamic> args = const {}]) {
    try {
      final manager = XyzConfigManager._translationManager!;
      final fields = manager._selected!.config._fields;
      final match = fields.entries.firstWhere((final l) {
        return l.key.toString().toLowerCase() == this.toLowerCase();
      });
      final h = _handle(
        match.value.toString(),
        {...fields, ...args},
        manager._sOpen,
        manager._sClose,
      );
      return let<T>(h);
    } catch (_) {}
    return null;
  }
}
